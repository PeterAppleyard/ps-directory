	<!-- Section 01 — The Founding -->
	<section class="border-b-2 border-stone-900 bg-white">
		<div class="mx-auto max-w-6xl divide-y-2 md:divide-y-0 md:grid md:grid-cols-[280px_1fr] md:divide-x-2 divide-stone-900">
			<div class="px-8 py-12 md:py-16">
				<p class="font-black leading-none text-stone-100 select-none"
					style="font-size: clamp(5rem, 10vw, 9rem); line-height: 1;">01</p>
				<h2 class="mt-3 font-black text-2xl tracking-tight text-stone-900 md:text-3xl">
					The Founding
				</h2>
			</div>
			<div class="px-8 py-12 md:py-16 space-y-5 text-stone-600 leading-relaxed text-[15px]">
				<p>
					In 1961, a project home company called <strong class="font-bold text-stone-900">Sun-Line Homes</strong> entered liquidation.
					Two of its executives — <strong class="font-bold text-stone-900">Brian Pettit</strong>, an accountant, and
					<strong class="font-bold text-stone-900">Ron Sevitt</strong>, a Dublin-born salesman — saw opportunity in the collapse.
					Their initial brief was pragmatic: finish the houses Sun-Line had taken deposits for, and use those proceeds to build their own operation.
				</p>
				<p>
					Their mission was clear: make <strong class="font-bold text-stone-900">architect-designed homes accessible</strong> to young professionals and middle-class families — people who appreciated good design but couldn't afford a private architect.
				</p>
				<p>
					Their first move was brilliant. They commissioned two young architects working at the NSW State Government —
					<strong class="font-bold text-stone-900">Ken Woolley</strong> and <strong class="font-bold text-stone-900">Michael Dysart</strong> — who had just won Australia's Family Home Competition for a low-cost house design in 1958.
				</p>
				<p>
					The first two Split Level houses — built at Clifton Gardens and St Ives in early 1962 — generated enough business and public interest to finance an exhibition centre. By 1963 P&amp;S had opened their first display village at Carlingford; by 1964 the flagship <strong class="font-bold text-stone-900">St Ives display village</strong> on Richmond Road had become a weekend destination for Sydney families, with architecture students touring the homes as teaching examples.
				</p>
			</div>
		</div>
	</section>

	<!-- Section 02 — The Architect -->
	<section class="border-b-2 border-stone-900 bg-white">
		<div class="mx-auto max-w-6xl">
			<!-- Label row -->
			<div class="border-b-2 border-stone-900 px-8 py-8 flex items-baseline gap-6">
				<span class="font-black leading-none text-stone-100 select-none"
					style="font-size: clamp(4rem, 8vw, 7rem); line-height: 1;">02</span>
				<h2 class="font-black text-3xl tracking-tight text-stone-900 md:text-4xl">
					The Architect
				</h2>
			</div>
			<div class="px-8 py-12 md:py-16 space-y-5 text-stone-600 leading-relaxed text-[15px] max-w-3xl">
				<p>
					<strong class="font-bold text-stone-900">Ken Woolley</strong> (1933–2015) was the creative force behind Pettit &amp; Sevitt for fifteen years. Graduating from the University of Sydney in 1955 with First Class Honours, the University Medal, and a Byera Hadley Travelling Scholarship that took him through the USA, Europe, and UK, Woolley brought genuine international ambition to what was then a modest local industry.
				</p>
				<p>
					He designed over <strong class="font-bold text-stone-900">3,500 P&amp;S homes</strong> — making him arguably Australia's most prolific residential architect. What set P&amp;S apart from every other project builder was that buyers didn't deal with a salesman. They met the architect directly.
				</p>
				<div class="border-l-4 border-stone-900 pl-5 my-6">
					<p class="text-[13px] font-bold tracking-normal text-stone-500 uppercase mb-2">The P&amp;S Consultation</p>
					<p class="text-[15px] text-stone-700 leading-relaxed">Buyers met personally with Woolley or one of his peers to discuss their site, orientation, sun, views, and modifications to the standard plan. Kitchen configurations, built-in furniture, window placement — all negotiable. You weren't just buying a house, you were working with Australia's leading architects to design your home.</p>
				</div>
				<p>
					Woolley won the Wilkinson Award — New South Wales' highest residential architecture prize — <strong class="font-bold text-stone-900">four times</strong>, the only architect ever to achieve this. He received the RAIA Gold Medal in 1993, Australia's highest architectural honour, and was posthumously described as "the most complete architect of the modern era in Australia." He is as known for the Fisher Library at Sydney University and the Park Hyatt at Campbell's Cove as he is for the houses that shaped Sydney's North Shore.
				</p>
			</div>
		</div>
	</section>

	<!-- Section 03 — The Houses -->
	<section class="border-b-2 border-stone-900 bg-white">
		<div class="mx-auto max-w-6xl">
