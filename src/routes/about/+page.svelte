				<h2 class="mt-3 font-black text-xl tracking-tight text-stone-900">The Company</h2>
			</div>
			<div class="space-y-5 text-stone-600 leading-relaxed text-[15px] max-w-2xl">
				<p>
					Founded in 1961 by <strong class="font-bold text-stone-900">Ron Sevitt</strong> and <strong class="font-bold text-stone-900">Brian Pettit</strong>, Pettit &amp; Sevitt emerged from the collapse of Sun-Line Homes with a clear mission: make architect-designed modern homes accessible to middle-class Australians.
				</p>
				<p>
					At a time when most affordable housing meant fibro cottages or basic project homes, Pettit &amp; Sevitt took a different approach. Architects <strong class="font-bold text-stone-900">Ken Woolley</strong> and <strong class="font-bold text-stone-900">Michael Dysart</strong> were commissioned to design the company's foundational models. Together, they created the Split Level — a groundbreaking design that worked economically on Sydney's challenging sloping sites. Dysart had previously won Australia's Family Home Competition with Woolley in 1958, bringing expertise in affordable, well-designed housing. Woolley continued as the primary designer from 1961 to 1975, while other architects including Russell Jack and Neil Clerehan also contributed designs.
				</p>
				<p>
					Approximately <strong class="font-bold text-stone-900">3,500 homes</strong> were built between 1961 and the company's closure in the late 1970s. The Split Level and the award-winning Lowline became the most popular models. These homes were built using standard Australian materials — brick veneer, Monier tiles, Gyprock, Stegbar windows — but through thoughtful design, elevated them to architecture.
				</p>
				<p>
					The display villages at Carlingford and St Ives became weekend destinations for Sydney families exploring modern living. Architecture students toured the exhibitions. The homes appeared in magazines, advertising campaigns featuring photography by <strong class="font-bold text-stone-900">Max Dupain</strong>, and films including Bruce Beresford's <em>Don's Party</em>.
				</p>
				<p>
					Pettit &amp; Sevitt homes were part of what became known as the <strong class="font-bold text-stone-900">Sydney School</strong> — a loose collective of architects whose work emphasised natural materials, connection to landscape, and climate-appropriate design. Though the company closed in 1977–78, the homes remain highly valued today. Many still stand across Sydney's North Shore, Canberra, and beyond — testament to quality design that has endured for over 60 years.
				</p>
