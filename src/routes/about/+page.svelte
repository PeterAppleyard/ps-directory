				<h2 class="font-black text-3xl tracking-tight text-stone-900 md:text-4xl">
					The Houses
				</h2>
			</div>
			<!-- Four model panels -->
			<div class="grid grid-cols-1 divide-y-2 md:divide-y-0 md:grid-cols-2 md:divide-x-2 divide-stone-900">
				<div class="group px-8 py-12 transition-colors duration-200 hover:bg-stone-900 border-b-2 border-stone-900 md:border-b-0">
					<p class="mb-4 text-[10px] font-bold tracking-normal text-accent transition-colors group-hover:text-stone-500">
						Multi-level &nbsp;·&nbsp; 1962
					</p>
					<h3 class="mb-5 font-black text-5xl tracking-tight text-stone-900 transition-colors group-hover:text-white md:text-6xl">
						Split&#8209;level
					</h3>
					<p class="text-sm leading-relaxed text-stone-500 transition-colors group-hover:text-stone-400">
						Designed for Sydney's uneven, sloping, rocky terrain. Instead of expensive cut-and-fill earthworks, the Split Level followed the land — each floor offset by half a storey, creating distinct zones for living, sleeping, and service. Woolley called it "an expression of the principles of modern architecture" specific to Sydney's hilly suburbs.
					</p>
				</div>

				<div class="group px-8 py-12 transition-colors duration-200 hover:bg-stone-900 border-b-2 border-stone-900 md:border-b-2 md:border-b-stone-900">
					<p class="mb-4 text-[10px] font-bold tracking-normal text-accent transition-colors group-hover:text-stone-500">
						Single storey &nbsp;·&nbsp; 1963 &nbsp;·&nbsp; Most popular
					</p>
					<h3 class="mb-5 font-black text-5xl tracking-tight text-stone-900 transition-colors group-hover:text-white md:text-6xl">
						Low&shy;line
					</h3>
					<p class="text-sm leading-relaxed text-stone-500 transition-colors group-hover:text-stone-400">
						Low-pitched roof, strong horizontal emphasis, open-plan living flowing out to garden and deck. The most iconic P&amp;S silhouette — hugging the landscape rather than competing with it. Woolley's Lowline B won the 1967 RAIA Project House Design Award. Multiple configurations were offered, from compact three-bedders to larger entertainer layouts.
					</p>
				</div>

				<div class="group px-8 py-12 transition-colors duration-200 hover:bg-stone-900 border-b-2 border-stone-900 md:border-b-0 md:border-t-2 md:border-stone-900">
					<p class="mb-4 text-[10px] font-bold tracking-normal text-accent transition-colors group-hover:text-stone-500">
						Two storey &nbsp;·&nbsp; Late 1960s
					</p>
					<h3 class="mb-5 font-black text-5xl tracking-tight text-stone-900 transition-colors group-hover:text-white md:text-6xl">
						High&shy;line
					</h3>
					<p class="text-sm leading-relaxed text-stone-500 transition-colors group-hover:text-stone-400">
						The vertical counterpart to the Lowline. Steep rooflines, two full storeys, a ground floor of living and kitchen flowing up to bedrooms above. For buyers wanting clear separation of living and sleeping zones, or sites where height captured a better view. Dramatic entrance stairs and cantilevered balconies gave it a more assertive presence on the street.
					</p>
				</div>

				<div class="group px-8 py-12 transition-colors duration-200 hover:bg-stone-900 md:border-t-2 md:border-stone-900">
					<p class="mb-4 text-[10px] font-bold tracking-normal text-accent transition-colors group-hover:text-stone-500">
						Inward-facing &nbsp;·&nbsp; c. 1965
					</p>
					<h3 class="mb-5 font-black text-5xl tracking-tight text-stone-900 transition-colors group-hover:text-white md:text-6xl">
						Court&shy;yard
					</h3>
					<p class="text-sm leading-relaxed text-stone-500 transition-colors group-hover:text-stone-400">
						A house turned inward. All main rooms — living, kitchen, bedrooms — face an enclosed courtyard rather than the street. Brick walls on two sides, the house on the other two. The roof slopes toward the courtyard, emphasising the relationship. A private outdoor room visible from every corner of the house. Ideal for urban blocks, young families, and Sydney's unpredictable weather.
					</p>
				</div>
			</div>
			<!-- Modular system strip -->
			<div class="border-t-2 border-stone-900 bg-stone-50 px-8 py-8 md:flex md:items-start md:gap-12">
				<div class="shrink-0 mb-4 md:mb-0">
					<p class="text-[10px] font-bold tracking-normal text-stone-400 uppercase">The System</p>
					<p class="font-black text-xl tracking-tight text-stone-900 mt-1">Modular Grid</p>
				</div>
				<p class="text-sm text-stone-500 leading-relaxed flex-1">
					Every P&amp;S model was built on a <strong class="font-semibold text-stone-700">3-foot module</strong> — a structural grid that made components interchangeable, rooflines swappable, and rooms expandable by simply adding modules. Woolley described it: <em>"The large grid and its section set up a rhythm like a beat in music, around which a composition of infill elements is composed."</em> The system enabled the company to build from contract signing to lock-up in <strong class="font-semibold text-stone-700">eight weeks</strong> — while still feeling custom to every buyer.
				</p>
			</div>
		</div>
	</section>

	<!-- Section 04 — The Sydney School -->
	<section class="border-b-2 border-stone-900 bg-white">
		<div class="mx-auto max-w-6xl divide-y-2 md:divide-y-0 md:grid md:grid-cols-[280px_1fr] md:divide-x-2 divide-stone-900">
			<div class="px-8 py-12 md:py-16">
				<p class="font-black leading-none text-stone-100 select-none"
					style="font-size: clamp(5rem, 10vw, 9rem); line-height: 1;">04</p>
				<h2 class="mt-3 font-black text-2xl tracking-tight text-stone-900 md:text-3xl">
					The Sydney School
				</h2>
				<p class="mt-4 text-xs leading-relaxed text-stone-400">
					Also known, somewhat unkindly, as "Nuts and Berries" — a regionalist modernism that rejected the white-box purity of International Style in favour of natural materials, site response, and the Australian bush.
				</p>
			</div>
			<div class="px-8 py-12 md:py-16">
				<!-- Five characteristics -->
				<div class="space-y-0 divide-y divide-stone-200">
					<div class="flex gap-5 py-5">
						<span class="shrink-0 font-black text-stone-200 text-2xl w-8 text-right select-none">1</span>
						<div>
							<p class="font-bold text-stone-900 text-sm">Site Response</p>
							<p class="text-sm text-stone-500 leading-relaxed mt-0.5">Buildings follow the land rather than flattening it. Split-level planning eliminates cut-and-fill. Existing trees and rock outcrops are incorporated, not removed.</p>
						</div>
					</div>
					<div class="flex gap-5 py-5">
						<span class="shrink-0 font-black text-stone-200 text-2xl w-8 text-right select-none">2</span>
						<div>
							<p class="font-bold text-stone-900 text-sm">Natural Materials</p>
							<p class="text-sm text-stone-500 leading-relaxed mt-0.5">Clinker brick (burnt, imperfect, textured), western red cedar boarding and panelling, dark Monier concrete roof tiles, Sydney sandstone for retaining walls. Materials shown honestly — not painted, not hidden.</p>
						</div>
					</div>
					<div class="flex gap-5 py-5">
						<span class="shrink-0 font-black text-stone-200 text-2xl w-8 text-right select-none">3</span>
						<div>
							<p class="font-bold text-stone-900 text-sm">Structural Honesty</p>
							<p class="text-sm text-stone-500 leading-relaxed mt-0.5">Post-and-beam construction left exposed. Oregon and Douglas Fir rafters visible from below. The structure tells you how the building stands up — no suspended ceilings hiding the craft above.</p>
						</div>
					</div>
					<div class="flex gap-5 py-5">
						<span class="shrink-0 font-black text-stone-200 text-2xl w-8 text-right select-none">4</span>
						<div>
							<p class="font-bold text-stone-900 text-sm">Indoor–Outdoor Flow</p>
							<p class="text-sm text-stone-500 leading-relaxed mt-0.5">Full-height glazing, clerestory windows for light and ventilation, timber decks at multiple levels. The boundary between house and garden is deliberately blurred — a response to Sydney's climate, not imported from Europe.</p>
						</div>
					</div>
					<div class="flex gap-5 py-5">
						<span class="shrink-0 font-black text-stone-200 text-2xl w-8 text-right select-none">5</span>
						<div>
							<p class="font-bold text-stone-900 text-sm">Bush Integration</p>
							<p class="text-sm text-stone-500 leading-relaxed mt-0.5">Houses nestle into landscape rather than dominating it. Landscape architect <strong class="font-semibold">Bruce Mackenzie</strong> shaped P&amp;S display gardens — bolstering native vegetation, using stone retaining walls laid by hand, creating settings where the house appears to have grown from the site.</p>
						</div>
					</div>
				</div>
				<p class="mt-8 text-[11px] font-bold tracking-normal text-stone-400">
					Influences &nbsp;·&nbsp; Frank Lloyd Wright (organic architecture) &nbsp;·&nbsp; Alvar Aalto (humanist modernism) &nbsp;·&nbsp; Japanese post-and-beam tradition
				</p>
			</div>
		</div>
	</section>

