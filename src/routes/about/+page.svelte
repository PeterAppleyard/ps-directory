			<div class="grid grid-cols-1 divide-y-2 md:divide-y-0 md:grid-cols-3 md:divide-x-2 divide-stone-900">
				<div class="px-8 py-12 space-y-4">
					<p class="text-[10px] font-bold tracking-normal text-stone-400 uppercase">The Eichler Comparison</p>
					<p class="text-sm leading-relaxed text-stone-600">
						In California, <strong class="font-semibold text-stone-900">Joseph Eichler</strong> commissioned leading architects — Quincy Jones, Robert Anshen — to build over 11,000 mid-century modern homes for middle-class families. P&amp;S did the same in Sydney, at 3,500 homes, with Woolley leading the design effort. Same era, same mission, same enduring desirability. Eichler influenced Steve Jobs' belief in design for the many; P&amp;S shaped how an entire generation of Sydneysiders understood what a home could be.
					</p>
				</div>
				<div class="px-8 py-12 space-y-4">
					<p class="text-[10px] font-bold tracking-normal text-stone-400 uppercase">The Decline &amp; Revival</p>
					<p class="text-sm leading-relaxed text-stone-600">
						The 1973 sale to Landall/CHI brought corporate priorities. When Woolley departed in 1975, the design vision went with him. Inflation, land scarcity, and the energy crisis squeezed margins. The company entered liquidation in 1978.
					</p>
					<p class="text-sm leading-relaxed text-stone-600">
						In 2010, <strong class="font-semibold text-stone-900">Val Sevitt</strong> — Ron Sevitt's widow — relaunched Pettit+Sevitt with her children Carol and Colin. At 77, Ken Woolley revisited and updated his Lowline design for contemporary living. The principles held. The company continues operating today.
					</p>
				</div>
				<div class="px-8 py-12 space-y-4">
					<p class="text-[10px] font-bold tracking-normal text-stone-400 uppercase">Preservation Today</p>
					<p class="text-sm leading-relaxed text-stone-600">
						P&amp;S homes are now 45–65 years old. The community that surrounds them is active — the <strong class="font-semibold text-stone-900">Pettit and Sevitt Owners and Friends Club</strong> on Facebook, renovation specialists <strong class="font-semibold text-stone-900">Studio Prineas</strong> and <strong class="font-semibold text-stone-900">Dr Retro</strong>, and growing numbers of individual heritage listings as councils recognise their significance.
					</p>
					<p class="text-sm leading-relaxed text-stone-600">
						The threat is real: demolition for McMansions, rendered brick, added gables. A Piper Press book by <strong class="font-semibold text-stone-900">Kathryn Franco</strong> — with 200+ illustrations and previously unpublished plans — is in production to document the full story before more is lost.
					</p>
				</div>
			</div>
